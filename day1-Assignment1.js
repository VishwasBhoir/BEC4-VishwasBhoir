const add = (num1, num2) => {
    console.log(`${num1} + ${num2} = ${num1 + num2}`);
};

const sub = (num1, num2) => {
    console.log(`${num1} - ${num2} = ${num1 - num2}`);
};

const mul = (num1, num2) => {
    console.log(`${num1} x ${num2} = ${num1 * num2}`);
};

const div = (num1, num2) => {
    console.log(`${num1} / ${num2} = ${num1 / num2}`);
};

let num1 = 30;
let num2 = 10;

add(num1, num2);
sub(num1, num2);
mul(num1, num2);
div(num1, num2);
